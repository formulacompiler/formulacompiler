package org.formulacompiler.tests.utils;

import java.io.InputStream;

import junit.framework.Assert;

public class IOAssert extends Assert
{
	public static void assertEqualStreams( String _message, InputStream _expected, InputStream _actual ) throws Exception
	{
		int offset = 0;
		while (true) {
			final int e = _expected.read();
			final int a = _actual.read();
			if (e != a) {
				assertEquals( _message + " at offset " + offset, e, a );
			}
			offset++;
			if (e < 0) break;
		}
	}
}
